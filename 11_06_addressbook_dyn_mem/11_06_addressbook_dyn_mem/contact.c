int FindByname(const Contact* pc, char name[]) {
	int i = 0;


	for (i = 0; i < pc->sz; i++) {
		if (strcmp(pc->data[i].name, name) == 0) {
			return i;
		}

	}
	return -1;//找不到了
}


void DelContact(Contact* pc) {
	if (pc->sz == 0) {
		printf("通讯录是空的\n");
		return 0;

	}

	char name[MAX_NAME] = { 0 };
	assert(pc);

	printf("请输入要删除的人的名字：>");
	scanf("%s", &name);
	
	int del = FindByname(pc, name);

	if (del == -1) {
		printf("要删除的联系人不存在\n");
		return;
	}
	int i = 0;
	for (i = del; i < pc->sz - 1; i++) {
		pc->data[i] = pc->data[i + 1];
	}
	pc->sz--;

	printf("成功删除联系人\n");
}

